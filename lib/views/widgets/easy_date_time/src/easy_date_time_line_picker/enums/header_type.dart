/// An enumeration that defines the types of headers that can be used in the
/// EasyDateTimeLinePicker widget.
///
/// The available header types are:
///
/// - `viewOnly`: A header that is used for display purposes only.
/// - `picker`: A header that allows the user to pick a date.
/// - `none`: No header is displayed.
enum HeaderType {
  /// A header that is used for display purposes only.
  viewOnly,

  /// A header that allows the user to pick a date.
  picker,

  /// No header is displayed.
  none,
}
